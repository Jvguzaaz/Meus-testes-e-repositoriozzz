numerox = input ("Digite um número: ")
numeroy = input ("Digite outro número: ")

soma = int(numerox) + int(numeroy)
print ("A soma é: " + str(soma))

subtracao = int(numerox) - int(numeroy)
print ("A subtração é: " + str(subtracao))

multiplicacao = int(numerox) * int(numeroy)
print ("A multiplicação é: " + str(multiplicacao))

divisao = int(numerox) / int(numeroy)
print ("A divisão é: " + str(divisao))